    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
if(id.getText().equals("")){
    JOptionPane.showMessageDialog(this, "All field is required");
    id.requestFocus();
}
else if(name.getText().equals("")){
    JOptionPane.showMessageDialog(this, "All field is required");
    name.requestFocus();
}
else if(course.getText().equals("")){
    JOptionPane.showMessageDialog(this, "All field is required");
    course.requestFocus();
}
else if(sem.getText().equals("")){
    JOptionPane.showMessageDialog(this, "All field is required");
    sem.requestFocus();
}
else if(branch.getText().equals("")){
    JOptionPane.showMessageDialog(this, "All field is required");
    branch.requestFocus();
}
else{
   
        Connection con=connect.Connection();
        PreparedStatement pst;
    try {
        pst = con.prepareStatement("insert into student(id,name,course,semester,branch) value(?,?,?,?,?)");
         pst.setString(1, id.getText());
        pst.setString(2, name.getText());
        pst.setString(3, course.getText());
        pst.setString(4, sem.getText());
        pst.setString(5, branch.getText());
        pst.executeUpdate();
        JOptionPane.showMessageDialog(this, "Record saved");
        id.setText("");
         name.setText("");
          course.setText("");
           sem.setText("");
            branch.setText("");
    } catch (SQLException ex) {
        Logger.getLogger(StudentReg.class.getName()).log(Level.SEVERE, null, ex);
    }
       
    
}
    }//GEN-LAST:event_saveActionPerformed

